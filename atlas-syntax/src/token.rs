use std::fmt::Display;

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Eof,
    Identifier(String),
    Float(f64),
    Int(i64),
    String(String),
    Char(char),
    Plus,
    Minus,
    Star,
    Slash,
    Modulo,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Comma,
    Semicolon,
    Colon,
    Equal,
    DoubleEqual,
    NotEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    And,
    Or,
    Not,
    Return,
    If,
    Else,
    Break,
    Continue,
    Function,
    Let,
    True,
    False,
    Nil,
    Arrow,
    FatArrow,
    NewLine,
    As,
    VoidType,
    I32Type,
    F32Type,
    CharType,
    StringType,
}


pub enum Token {
    // Single-character tokens.
    LeftParen,
    RightParen,
    LeftBrace,
    RightBrace,
    LeftBracket,
    RightBracket,
    Comma,
    Dot,
    Minus,
    Plus,
    Semicolon,
    Slash,
    Star,

    // One or two character tokens.
    Bang,
    BangEqual,
    Equal,
    EqualEqual,
    Greater,
    GreaterEqual,
    Less,
    LessEqual,
    Arrow,
    FatArrow,

    // Literals.
    Identifier(String),
    String(String),
    Number(f64),

    // Keywords.
    And,
    Struct,
    Else,
    False,
    Fun,
    If,
    Nil,
    Or,
    Print,
    Return,
    This,
    True,
    Let,
    Include,

    // Other.
    Eof,
    UnterminatedString,
    Unknown(char),
}